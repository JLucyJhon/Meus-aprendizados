// #include : serve para inserir bibliotecas
#include <iostream> // biblioteca de entrada e saída

using namespace std; // indica aonde estão as bibliotecas que serão usadas no código, organização de pastas.

int main() // função booleana, caso ache o arquivo, retorna como true, se não achar, retorna como else.
{
    cout << "parabens, você conseguiu, agora vá embora" << endl; // saída do conteudo na tela, o endl serve para quebrar a linha durante o compilamento do código
    system("pause"); // serve para realizar uma pausa no sistema enquanto ele é rodado, para que você possa teclar antes de parar.
    return 0;
}
